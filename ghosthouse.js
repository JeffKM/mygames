print('그런데 뒤를 보니 입구가 없습니다. 어쩔수 없이 귀신의 집을 들어가보기로 합니다.');
print('귀신의 집의 문을 엽니다. 깜깜하고 조용합니다');

var q2 = prompt("어떤 불을 키시겠습니까? - 1)핸드폰 불빛 2)성냥 불빛");

clear();

if (q2 == "2") {
	print('성냥에 불을 붙입니다.');
	print('성냥의 불이 뜨거워 성냥을 떨어뜨렷습니다.');
	print('귀신의 집에 불이 났습니다.');
	process.exit();
}

print('핸드폰 불빛을 킵니다.');
