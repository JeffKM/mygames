var q3 = prompt("어디로 가시겠습니까? - 1)1층 통로를 먼저 보기로 한다. 2)2층에 올라가보기로 한다.");

clear();
if (q3 == "2") {
	print('계단을 올라갑니다.');
	print('갑자기 계단이 무너졌습니다.');
	process.exit();
}

